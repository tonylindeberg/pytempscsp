def contlimitkern_mean(
        stddev : float,
        c: float = 2.0
) -> np.ndarray:
    """Returns the temporal mean of the continuous time-causal limit kernel
    according to Equation (34) in 

    Lindeberg (2016) "Time-causal and time-recursive spatio-temporal receptive 
    fields", Journal of Mathematical Imaging and Vision 55(1): 50-88.
    """
    return sqrt((c + 1)/(c - 1)) * stddev


def map_contlimitkern_mean(
        stddevvec : np.ndarray,
        c: float = 2.0
) -> np.ndarray:
    """Returns the temporal mean of the continuous time-causal limit kernel for 
    an array with values of the standard deviation stddev of the 
    time-causal limit kernel."""
    out = np.zeros(len(stddevvec))

    for i, stddev in np.ndenumerate(stddevvec):
        out[i] = \
          contlimitkern_mean(stddev, c)

    return out


def contlimitkern_tmax(
        stddev : float,
        c: float = 2.0
) -> np.ndarray:
    """Returns an estimate of the the temporal mean of the continuous 
    time-causal limit kernel according to Equation (39) in

    Lindeberg (2023) "A time-causal and time-recursive scale-covariant
    scale-space representation of temporal signals and past time", 
    Biological Cybernetics, 117(1-2): 21-59.
    """
    return ((c + 1)**2 * stddev)/(2 * sqrt(2) * sqrt((c - 1) * c**3))


def map_contlimitkern_tmax(
        stddevvec : np.ndarray,
        c: float = 2.0
) -> np.ndarray:
    """Returns the temporal mean of the continuous time-causal limit kernel for 
    an array with values of the standard deviation stddev of the 
    time-causal limit kernel."""
    out = np.zeros(len(stddevvec))

    for i, stddev in np.ndenumerate(stddevvec):
        out[i] = \
          contlimitkern_tmax(stddev, c)

    return out
